package StringsInJava;

public class PrettyPrinting {
    public static void main(String[] args) {

        //Formatted Printings in Java

        float num = 12.919951f;
        System.out.printf("The num is %.2f\n", num);

        System.out.printf("Pie value is %.3f\n", Math.PI);

        System.out.printf("Hello My name is %s an iam a %s person\n", "Rahul","Cool");

    }
}
